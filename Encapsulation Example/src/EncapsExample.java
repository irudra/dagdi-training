public class EncapsExample {
    private String name;
    private String idNum;
    private int age;

    public String getName()
    {
       return name;
    }
    public String getIdNum()
    {
        return idNum;
    }
    public int getAge()
    {
        return age;
    }

    public void setName(String newName)
    {
      name=newName;
    }
    public void setIdNum(String newIdNum)
    {
        idNum=newIdNum;
    }
    public void setAge(int newAge)
    {
        age=newAge;
    }
}
